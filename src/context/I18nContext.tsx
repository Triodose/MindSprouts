export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<Language>(() => {
    // 1. Check local storage
    const saved = localStorage.getItem('mindsprout_language') as Language;
    if (saved === 'zh' || saved === 'en' || saved === 'ja') {
      return saved;
    }

    // 2. Check browser default language
    const browserLang = navigator.language.toLowerCase();
    if (browserLang.includes('zh')) return 'zh';
    if (browserLang.includes('ja')) return 'ja';
    return 'en';
  });

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem('mindsprout_language', lang);
  };

  const t = (key: string): string => {
    const dict = translations[language];
    // Fallback to English, then back to the key name if not found
    return (dict as any)[key] || (translations.en as any)[key] || key;
  };

  return (
    <I18nContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </I18nContext.Provider>
  );
};

export const useI18n = () => {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within an I18nProvider');
  }
  return context;
};
